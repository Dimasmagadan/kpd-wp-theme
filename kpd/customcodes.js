(function() {
    tinymce.create('tinymce.plugins.adsense_1', {
        init : function(ed, url) {
            ed.addButton('adsense_1', {
                title : 'Добавить код Адсенса №1',
                 image : url+'/img/i1.png',
                onclick : function() {
                     ed.selection.setContent('[adsense n=1]');
                }
            });
        },
        createControl : function(n, cm) {
            return null;
        }
    });
    tinymce.PluginManager.add('adsense_1', tinymce.plugins.adsense_1);
})();

